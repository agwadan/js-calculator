
/* Function to add */
const add = (x, y) => {
  return x + y;
 }
const subtract = (x, y) => {
  return x - y;
}
const multiply = (x, y) => {
  return x * y;
}
const divide = (x, y) => {
  return x / y;
}

console.log(add(2, 3))
console.log(subtract(4, 2));
multiply(3, 2);
console.log(divide(10, 5));